class PrintingComments extends ConsoleProgram {
  /**
  * A printing and comments example
  * @author:
  */
  
  
  public void run() {

    // Say hello
    System.out.println("Hi");
    System.out.println("There.");

    // Say hello on the same line
    System.out.print("Hi ");
    System.out.print("There.");

    /*
    this paragraph
    will be ignored
    It is a multiline comment
    */
    

  }
}
